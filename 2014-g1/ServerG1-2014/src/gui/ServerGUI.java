    private boolean validan(int broj) {
        int brojac = 0;
        for (Integer br : listaBrojeva) {
            if(br == broj) brojac++;
        }
        
        if(brojac < 2){
            return true;
        }else{
            return false;
        }
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    }

