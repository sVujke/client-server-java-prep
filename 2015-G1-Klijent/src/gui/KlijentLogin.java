    private void jbtPogodiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtPogodiActionPerformed
        // TODO add your handling code here:
        String slovo = jtxtSlovo.getText();
        if(slovo.length() >1){
            JOptionPane.showConfirmDialog(rootPane, "samo jedno slovo!");
            
        }else{
            
            if(koriscenaSlova.contains(slovo)){
                JOptionPane.showMessageDialog(rootPane, "vec ima to slovo");
            }else{
                koriscenaSlova.add(slovo);
            jlblSlova.setText(koriscenaSlova.toString());
            out.println(slovo);
            brPokusaja++;
            jlblPokusaji.setText(jlblPokusaji.getText()+" "+brPokusaja);
            if(brPokusaja == jlblServer.getText().length()+8){
                jbtPogodi.setEnabled(false);
                JOptionPane.showMessageDialog(rootPane, "Nema vise pokusaja");
            }
            }
            
        }
        
        
    }//GEN-LAST:event_jbtPogodiActionPerformed

