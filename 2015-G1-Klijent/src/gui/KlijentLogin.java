    private void jbtLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtLoginActionPerformed
        // TODO add your handling code here:
        String email = jtxtUser.getText().trim();
        String pass = jtxtPass.getText().trim();
        
            int validBr = valid(email, pass);
            
            switch(validBr){
                case 0: JOptionPane.showMessageDialog(rootPane, "Sve ok");
                break;
                case 1: JOptionPane.showMessageDialog(rootPane, "fali @");
                break;
                case 2: JOptionPane.showMessageDialog(rootPane, "nije dobra sifra");
                break;
                case 4: JOptionPane.showMessageDialog(rootPane, "greska");
                break;
                default: JOptionPane.showMessageDialog(rootPane, "Greska");
                break;
            }
            
            if(validBr == 0){
            try {
                soket = new Socket("localhost", 9000);
                in = new BufferedReader(new InputStreamReader(soket.getInputStream()));
                out = new PrintStream(soket.getOutputStream());
                String kredencijali = "###"+email+"/"+pass;
                out.println(kredencijali);
                
                String odgovor = in.readLine();
            while (true) {
                System.out.println(odgovor);
                if (odgovor.startsWith("ne")) {
                    JOptionPane.showMessageDialog(this, "Pogresno korisnicko ime!");
                    //korisnik = jtxtKorisnik.getText().trim();
                    return;
                } else if(odgovor.startsWith("ok")) {
                    //JOptionPane.showMessageDialog(this, odgovor);
                    jPanel1.setVisible(false);
                    jbtIzaberi.setEnabled(true);
                    jbtPogodi.setEnabled(true);
                    return;
                }else {
                    break;
                }
            }
            } catch (IOException ex) {
                //Logger.getLogger(KlijentLogin.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showConfirmDialog(rootPane, "Otkacen klijent");
            }
            }
        
        
    }//GEN-LAST:event_jbtLoginActionPerformed

