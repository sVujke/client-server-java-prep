    private void jbtUnesiBrojActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtUnesiBrojActionPerformed
        // TODO add your handling code here:
        int broj;
        try{
            broj = Integer.parseInt(jtxtBroj.getText());
        }catch( Exception ex){
            JOptionPane.showMessageDialog(rootPane, "niste uneli br");
            return;
        }
        
        if(validan(broj)){
        jtxtUnetiBrojevi.setText(jtxtUnetiBrojevi.getText()+" "+broj);
        listaBrojeva.add(broj);
        
        } else {
            JOptionPane.showMessageDialog(rootPane, "ne moze vise od 2 puta isti broj");
        }
        
    }//GEN-LAST:event_jbtUnesiBrojActionPerformed

    private void jbtPokreniServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtPokreniServerActionPerformed
        // TODO add your handling code here:
        if (listaBrojeva.size() == 4){
            //ServerNit nit = new ServerNit(listaBrojeva);
            if(!isSuspended()){
                nit.start();
                System.out.println("server pokrenut");
            } else {
                nit.resume();
                System.out.println("server opet pokrenut");
                susp = false;
            }
            
            jLabel3.setText("<html><font color=green>Server je pokrenut</font></html>");
            jbtZaustaviServer.setVisible(true);
            jbtUnesiBroj.setEnabled(false);
            jbtPokreniServer.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Mora da bude tacno 4 broja uneto");
        }
    }//GEN-LAST:event_jbtPokreniServerActionPerformed

